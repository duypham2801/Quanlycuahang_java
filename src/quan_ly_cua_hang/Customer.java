package quan_ly_cua_hang;

public class Customer {
    private String idCustomer;
    private String loginCustomer;
    private String passwordCustomer;
    private String nameCustomer;
    private String cccdCustomer;
    private String emailCustomer;
    private String addressCustomer;
    private String phoneCustomer;

    // Các phương thức getter và setter cho từng thuộc tính
    public String getIdCustomer() {
        return idCustomer;
    }

    public void setIdCustomer(String idCustomer) {
        this.idCustomer = idCustomer;
    }

    public String getLoginCustomer() {
        return loginCustomer;
    }

    public void setLoginCustomer(String loginCustomer) {
        this.loginCustomer = loginCustomer;
    }

    public String getPasswordCustomer() {
        return passwordCustomer;
    }

    public void setPasswordCustomer(String passwordCustomer) {
        this.passwordCustomer = passwordCustomer;
    }

    public String getNameCustomer() {
        return nameCustomer;
    }

    public void setNameCustomer(String nameCustomer) {
        this.nameCustomer = nameCustomer;
    }

    public String getCccdCustomer() {
        return cccdCustomer;
    }

    public void setCccdCustomer(String cccdCustomer) {
        this.cccdCustomer = cccdCustomer;
    }

    public String getEmailCustomer() {
        return emailCustomer;
    }

    public void setEmailCustomer(String emailCustomer) {
        this.emailCustomer = emailCustomer;
    }

    public String getAddressCustomer() {
        return addressCustomer;
    }

    public void setAddressCustomer(String addressCustomer) {
        this.addressCustomer = addressCustomer;
    }

    public String getPhoneCustomer() {
        return phoneCustomer;
    }

    public void setPhoneCustomer(String phoneCustomer) {
        this.phoneCustomer = phoneCustomer;
    }
}

