package quan_ly_cua_hang;

public class Staff {
    private String idStaff;
    private String loginStaff;
    private String passwordStaff;
    private String nameStaff;
    private String phoneStaff;
    private String cccdStaff;
    private String emailStaff;

    // Các phương thức getter và setter cho từng thuộc tính
    public String getIdStaff() {
        return idStaff;
    }

    public void setIdStaff(String idStaff) {
        this.idStaff = idStaff;
    }

    public String getLoginStaff() {
        return loginStaff;
    }

    public void setLoginStaff(String loginStaff) {
        this.loginStaff = loginStaff;
    }

    public String getPasswordStaff() {
        return passwordStaff;
    }

    public void setPasswordStaff(String passwordStaff) {
        this.passwordStaff = passwordStaff;
    }

    public String getNameStaff() {
        return nameStaff;
    }

    public void setNameStaff(String nameStaff) {
        this.nameStaff = nameStaff;
    }

    public String getPhoneStaff() {
        return phoneStaff;
    }

    public void setPhoneStaff(String phoneStaff) {
        this.phoneStaff = phoneStaff;
    }

    public String getCccdStaff() {
        return cccdStaff;
    }

    public void setCccdStaff(String cccdStaff) {
        this.cccdStaff = cccdStaff;
    }

    public String getEmailStaff() {
        return emailStaff;
    }

    public void setEmailStaff(String emailStaff) {
        this.emailStaff = emailStaff;
    }
}

